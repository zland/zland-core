 /**
  *
  * @filedescription zland change event emitter used in flux stores
  * (and where an event emitter is needed)
  *
  */

 'use strict';

var EventEmitter = require('events').EventEmitter;
var CHANGE_EVENT = 'change';
var assign = require('object-assign');

var ChangeEventEmitter = assign({}, EventEmitter.prototype, {

  emitChange: function() {
    this.emit(CHANGE_EVENT);
  },

  /**
   * @param {function} callback
   */
  addChangeListener: function(callback) {
    this.on(CHANGE_EVENT, callback);
  },

  /**
   * @param {function} callback
   */
  removeChangeListener: function(callback) {
    this.removeListener(CHANGE_EVENT, callback);
  }
});

ChangeEventEmitter.setMaxListeners(2000);

module.exports = ChangeEventEmitter;
